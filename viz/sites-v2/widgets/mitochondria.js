  var JC1_ANCHOR_RATIO = 2.0;
  var JC1_ANCHOR_PSI = 150;
  var JC1_SLOPE = 24; // mV per doubling of ratio (illustrative)
  function psiFromJc1Ratio(ratio) {
    if (!(ratio > 0)) return null;
    return JC1_ANCHOR_PSI + JC1_SLOPE * (Math.log(ratio / JC1_ANCHOR_RATIO) / Math.LN2);
  }
  function jc1RatioFromPsi(psi) {
    return JC1_ANCHOR_RATIO * Math.pow(2, (psi - JC1_ANCHOR_PSI) / JC1_SLOPE);
  }

