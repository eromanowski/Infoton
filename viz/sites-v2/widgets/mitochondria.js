    function fitCanvas(canvas, aspect) {
      var rect = canvas.getBoundingClientRect();
      var cssW = rect.width || canvas.parentNode.clientWidth || 280;
      var cssH = cssW * (aspect || 0.82);
      var dpr = window.devicePixelRatio || 1;
      canvas.style.height = cssH + 'px';
      canvas.width = Math.round(cssW * dpr);
      canvas.height = Math.round(cssH * dpr);
      var ctx = canvas.getContext('2d');
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
      return { ctx: ctx, w: cssW, h: cssH };
    }

    function drawSpiral(r, progress, pulse) {
      var canvas = q('spiralCanvas');
      if (!canvas) return;
      if (progress === undefined) progress = 1;
      if (pulse === undefined) pulse = 0;
      var fit = fitCanvas(canvas, 0.82);
      var ctx = fit.ctx, w = fit.w, h = fit.h;
      ctx.clearRect(0, 0, w, h);

      var cx = w * 0.52;
      var cy = h * 0.46;
      var scale = Math.min(w, h) * 0.3;
      var zH = h * 0.36;
      var ax = 0.52;
      var cosA = Math.cos(ax);
      var sinA = Math.sin(ax);

      function project(x, y, z) {
        return {
          x: cx + (x - y) * cosA * scale,
          y: cy + (x + y) * sinA * scale - (z - 0.5) * zH,
        };
      }

      // faint floor grid for depth
      ctx.strokeStyle = 'rgba(167, 243, 208, 0.12)';
      ctx.lineWidth = 1;
      var g;
      for (g = -1; g <= 1; g += 0.5) {
        var a1 = project(g, -1, 0);
        var a2 = project(g, 1, 0);
        var b1 = project(-1, g, 0);
        var b2 = project(1, g, 0);
        ctx.beginPath(); ctx.moveTo(a1.x, a1.y); ctx.lineTo(a2.x, a2.y); ctx.stroke();
        ctx.beginPath(); ctx.moveTo(b1.x, b1.y); ctx.lineTo(b2.x, b2.y); ctx.stroke();
      }

      // coherence z-axis (back-left corner) with ticks + label
      var axisBase = project(-1, -1, 0);
      var axisTop = project(-1, -1, 1);
      ctx.strokeStyle = 'rgba(167, 243, 208, 0.3)';
      ctx.lineWidth = 1;
      ctx.beginPath(); ctx.moveTo(axisBase.x, axisBase.y); ctx.lineTo(axisTop.x, axisTop.y); ctx.stroke();
      ctx.fillStyle = 'rgba(220, 252, 231, 0.45)';
      ctx.font = "500 8px 'Cinzel', serif";
      ctx.textAlign = 'right';
      ctx.textBaseline = 'middle';
      var tick;
      for (tick = 0; tick <= 1.001; tick += 0.5) {
        var tp = project(-1, -1, tick);
        ctx.beginPath(); ctx.moveTo(tp.x, tp.y); ctx.lineTo(tp.x - 4, tp.y); ctx.stroke();
        ctx.fillText(tick.toFixed(1), tp.x - 6, tp.y);
      }
      ctx.save();
      ctx.translate(axisTop.x - 18, (axisTop.y + axisBase.y) / 2);
      ctx.rotate(-Math.PI / 2);
      ctx.textAlign = 'center';
      ctx.fillStyle = 'rgba(167, 243, 208, 0.6)';
      ctx.font = "600 9px 'Cinzel', serif";
      ctx.fillText('COHERENCE', 0, 0);
      ctx.restore();

      // ── Decoherence trajectory (damped quantum oscillator) ───────────
      //   ζ(t) = e^(−t/τ) · e^(iωt)   →   x = e^(−t/τ)cos(ωt), y = e^(−t/τ)sin(ωt)
      // z-axis = coherence amplitude e^(−t/τ). ω = r.omegaRadS, τ = r.tauCohS (both exact).
      // Q = ω·τ → cycles = ω·τ/2π = the true number of coherent oscillations. As ΔT → 0
      // (Δψ ≈ 138 mV) τ and Q diverge, so the VISIBLE turn count is capped at TURN_CAP to
      // stay legible; the window shrinks (uEnd) to keep the envelope honest. The exact Q is
      // always reported in the caption — only how much of the trajectory we draw is bounded.
      var rgb = stateRgb(r.state.id);
      var tauS = r.tauCohS;
      var omega = r.omegaRadS;
      var i, s, u, amp, phase, x, y, z, p, prev = null, hue, light;
      var collapsed = !(tauS > 0);

      var cycles = collapsed ? 0 : (omega * tauS) / (2 * Math.PI);
      var qRad = collapsed ? 0 : omega * tauS;
      var TURN_CAP = 5;
      var turnsShown = collapsed ? 0 : Math.min(cycles, TURN_CAP);
      var uEnd = cycles > 0 ? turnsShown / cycles : 0; // time window in units of τ (≤ 1)
      var N = collapsed ? 0 : Math.max(120, Math.min(900, Math.ceil(turnsShown * 64)));
      var drawN = Math.floor(N * progress);

      ctx.lineCap = 'round';
      for (i = 1; i <= drawN; i++) {
        s = i / N;
        u = uEnd * s;             // elapsed time in units of τ (bounded)
        amp = Math.exp(-u);       // coherence amplitude e^(−t/τ)
        phase = 2 * Math.PI * turnsShown * s;
        x = amp * Math.cos(phase);
        y = amp * Math.sin(phase);
        z = amp;
        p = project(x, y, z);
        if (prev) {
          hue = 140 * amp;            // green (coherent) → red (decohered)
          light = 44 + amp * 18;
          ctx.strokeStyle = 'hsla(' + hue + ', 78%, ' + light + '%, 1)';
          ctx.lineWidth = 2.4;
          ctx.beginPath();
          ctx.moveTo(prev.x, prev.y);
          ctx.lineTo(p.x, p.y);
          ctx.stroke();
        }
        prev = p;
      }

      // leading head dot while tracing
      if (!collapsed && progress < 1 && prev) {
        ctx.fillStyle = 'rgba(236, 253, 245, 0.9)';
        ctx.beginPath();
        ctx.arc(prev.x, prev.y, 3, 0, 2 * Math.PI);
        ctx.fill();
      }

      // decohered core marker — fades in at trace end, then pulses (faster when degraded)
      var markerAlpha = collapsed ? 1 : Math.max(0, Math.min(1, (progress - 0.8) / 0.2));
      if (markerAlpha > 0) {
        var endAmp = collapsed ? 0 : Math.exp(-uEnd);
        var end = collapsed
          ? project(0, 0, 0)
          : project(endAmp * Math.cos(2 * Math.PI * turnsShown), endAmp * Math.sin(2 * Math.PI * turnsShown), endAmp);
        var coh = coherenceUnit(r);
        var pulseRate = 1.4 + (1 - coh) * 2.6;
        var pulseAmt = 0.5 + 0.5 * Math.sin(pulse * pulseRate * Math.PI);
        var glowR = (11 + (1 - coh) * 4) + pulseAmt * (3 + (1 - coh) * 5);
        var grd = ctx.createRadialGradient(end.x, end.y, 1, end.x, end.y, glowR);
        grd.addColorStop(0, 'rgba(' + rgb[0] + ',' + rgb[1] + ',' + rgb[2] + ',' + (0.9 * markerAlpha) + ')');
        grd.addColorStop(1, 'rgba(' + rgb[0] + ',' + rgb[1] + ',' + rgb[2] + ',0)');
        ctx.fillStyle = grd;
        ctx.beginPath();
        ctx.arc(end.x, end.y, glowR, 0, 2 * Math.PI);
        ctx.fill();
        ctx.globalAlpha = markerAlpha;
        ctx.fillStyle = 'rgb(' + rgb[0] + ',' + rgb[1] + ',' + rgb[2] + ')';
        ctx.beginPath();
        ctx.arc(end.x, end.y, 5, 0, 2 * Math.PI);
        ctx.fill();
        ctx.globalAlpha = 1;
      }

      var cap = q('spiralCap');
      if (cap) {
        if (collapsed) {
          cap.textContent = 'coherence collapsed · ΔT ≤ 0 → τ → 0 (no sustained oscillation)';
        } else {
          cap.textContent = 'Q = ω·τ ≈ ' + qRad.toFixed(0) + ' rad → ' + cycles.toFixed(1) +
            ' coherent cycles · τ ' + fmtTau(r.tauCohPs);
        }
      }
    }

