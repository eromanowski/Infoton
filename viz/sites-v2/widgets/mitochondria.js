    function scheduleBeat() {
      if (!audio.on || !audio.ctx || !lastR) return;
      playThump(lastR);
      var coh = coherenceUnit(lastR);
      var base = 1100 - (1 - coh) * 720;
      var jitter = (1 - coh) * base * 0.5 * (Math.random() * 2 - 1);
      audio.timeout = setTimeout(scheduleBeat, Math.max(260, base + jitter));
    }
    function playThump(r) {
      var ctx = audio.ctx; if (!ctx) return;
      var coh = coherenceUnit(r);
      var now = ctx.currentTime;
      var collapsed = !(r.tauCohS > 0);
      function pulse(t, freq, peak, dur) {
        var osc = ctx.createOscillator(), g = ctx.createGain();
        osc.type = 'sine';
        osc.frequency.setValueAtTime(freq, t);
        osc.frequency.exponentialRampToValueAtTime(Math.max(30, freq * 0.6), t + dur);
        g.gain.setValueAtTime(0.0001, t);
        g.gain.exponentialRampToValueAtTime(peak, t + 0.008);
        g.gain.exponentialRampToValueAtTime(0.0001, t + dur);
        osc.connect(g); g.connect(ctx.destination);
        osc.start(t); osc.stop(t + dur + 0.02);
      }
      if (collapsed) { pulse(now, 38, 0.04, 0.28); return; }
      var baseFreq = 55 + coh * 50;
      var peak = 0.11 + coh * 0.12;
      var dur = 0.10 + coh * 0.15;
      pulse(now, baseFreq, peak, dur);
      pulse(now + 0.14, baseFreq * 0.78, peak * 0.7, dur * 0.9);
    }

